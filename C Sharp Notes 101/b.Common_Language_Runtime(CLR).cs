//C# programs run on .NET, a virtual execution system called the common language runtime (CLR) and a set of class libraries.
// Source code written in C# is compiled into an intermediate language (IL) that conforms to the CLI specification.



// Common Language Runtime.

// C# code is first translated to  CIL(Common Intermediate Language) or IL code(Intermediate Language)  by C# compiler.

// Then, it gets compiled as per Operating system differently in Windows, Linux or Macos.

// The IL code is compiled to machine(native) code by CLR.

// So, CLR is a program which translate the IL code to Native Code and this process is called Just-In-Time-Compilation.